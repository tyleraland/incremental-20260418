/* Coverage lens — test coverage per file, plus the highest-leverage cross view:
   complex AND untested. Coverage is a test-run artifact, so this lens is graceful
   when no coverage-final.json was generated. */
import * as lib from '../lib.js'

let ctx, moduleIds

export default {
  id: 'coverage', label: 'Coverage', kind: 'html', needs: ['coverage', 'complexity', 'modules'],
  mount(c) {
    ctx = c
    moduleIds = new Set(c.data.modules.nodes.map((n) => n.id))
    const COV = c.data.coverage
    if (!COV.available) {
      c.sidebar.innerHTML = '<div class="group-title">Coverage</div><p class="muted">Not generated.</p>'
      c.root.innerHTML =
        `<div class="stub"><h1>Coverage <span class="muted" style="font-size:14px">— not generated</span></h1>` +
        `<p>This lens reads <span class="mono">coverage/coverage-final.json</span>. It isn't in this build.</p>` +
        `<div class="callout"><b>Generate it</b><ol>` +
        `<li>Run <span class="mono">npm run coverage</span> (vitest with the v8 provider) at the repo root.</li>` +
        `<li>Re-run <span class="mono">npm run codemap</span>.</li>` +
        `</ol>CI generates it on deploy, so the hosted view always has coverage.</div></div>`
      return
    }

    const CX = c.data.complexity
    const s = COV.stats
    const bar = (v, color) => `<span class="bar" style="width:${Math.round((v || 0) * 1.4)}px;background:${color}"></span>`
    c.sidebar.innerHTML =
      '<div class="group-title">Coverage</div>' +
      `<div style="font-size:12px;line-height:2">` +
      `Statements <b>${s.statements}%</b><br>Branches <b>${s.branches}%</b><br>Functions <b>${s.functions}%</b></div>` +
      `<div class="muted" style="font-size:11px;margin-top:8px">${lib.fmtNum(s.files)} instrumented files</div>` +
      '<p class="muted" style="font-size:11px;margin-top:12px">A test-run artifact, not pure source — regenerated by CI on deploy.</p>'

    const covColor = (v) => lib.heat(1 - (v ?? 0) / 100)
    const fileCell = (p) => moduleIds.has(p) ? lib.goLink('modules', p, lib.short(p)) : `<span class="mono">${lib.short(p)}</span>`

    // complex AND untested: high max-cyclomatic × low coverage → best test targets
    const untested = Object.entries(COV.files).map(([path, cv]) => ({
      path, cov: cv.statements, uncovered: cv.uncovered, maxCc: CX.byFile?.[path]?.maxCyclomatic ?? 0,
      leverage: (CX.byFile?.[path]?.maxCyclomatic ?? 0) * (1 - (cv.statements ?? 0) / 100),
    })).filter((r) => r.leverage > 0).sort((a, b) => b.leverage - a.leverage).slice(0, 20)

    const least = Object.entries(COV.files).filter(([, v]) => v.statements != null)
      .map(([path, v]) => ({ path, ...v })).sort((a, b) => a.statements - b.statements).slice(0, 20)

    c.root.innerHTML =
      `<h3>Best test targets <span class="muted">— complex × untested</span></h3>` +
      `<p class="muted">High cyclomatic complexity and low coverage — the most logic per missing test.</p>` +
      `<table><thead><tr><th>file</th><th class="num">max CC</th><th class="num">coverage</th><th class="num">leverage</th></tr></thead><tbody>` +
      untested.map((r) => `<tr><td>${fileCell(r.path)}</td><td class="num">${r.maxCc}</td>` +
        `<td class="num" style="color:${covColor(r.cov)}">${r.cov}%</td><td class="num"><b>${r.leverage.toFixed(1)}</b></td></tr>`).join('') +
      `</tbody></table>` +

      `<h3>Least covered</h3>` +
      `<table><thead><tr><th>file</th><th class="num">stmts</th><th class="num">branches</th><th class="num">funcs</th><th></th></tr></thead><tbody>` +
      least.map((r) => `<tr><td>${fileCell(r.path)}</td>` +
        `<td class="num" style="color:${covColor(r.statements)}">${r.statements}%</td>` +
        `<td class="num">${r.branches ?? '–'}%</td><td class="num">${r.functions ?? '–'}%</td>` +
        `<td>${bar(r.statements, covColor(r.statements))}</td></tr>`).join('') +
      `</tbody></table>`

    c.root.querySelectorAll('a[data-go]').forEach((a) => a.addEventListener('click', () => c.go(a.dataset.go, a.dataset.arg)))
  },
}
